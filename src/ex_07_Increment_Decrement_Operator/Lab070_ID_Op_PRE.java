package ex_07_Increment_Decrement_Operator;

public class Lab070_ID_Op_PRE {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // Expression and Result Table
        // Line No // a // Result b
        //  8   /   10  /   NA
        //  9   /   11  /   11
        //  10  /   11  /   11
        //  11  /   11  /   11
    }
}
